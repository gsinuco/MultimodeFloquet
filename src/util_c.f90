  DOUBLE PRECISION, DIMENSION(A_dim1,A_dim2) :: A_FORTRAN
  
  write(*,*) 'fortan says: ',A_DIM
  
  
  DO I=1,A_DIM1
      DO J = 1,A_DIM2
